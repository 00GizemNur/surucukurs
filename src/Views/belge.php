                <h3>Tüm Ehliyet Sınıfları</h3>
                <p>
                    <b> A1 sınıfı sürücü belgesi nedir? A1 için yaş sınırı kaçtır?</b> <br><br>

                    2 Tekerlikli motosikletler için: <br>

                    Silindir hacmi 125 santimetreküpü geçmeyecek.
                    Güç olarak 11 kv(kilovat) sınır değerini aşmayacak.
                    Kv/kg oranı 0,1 değerini geçmeyecek şekilde sepetli veya sepetsiz olabilir. <br> <br>

                    3 Tekerlikli motosikletler için: <br>
                    Güç sınırı 15 kv(kilovat)’tır.
                    Yaş sınırı 16 olarak belirlenmiştir.
                    Aynı zamanda A1 sürücü belgesine sahip olanlar M sınıfına giren araçları da kullanabilir.
                    Yönetmelikte: " Silindir hacmi 125 santimetreküpü,  gücü 11 kilovatı ve gücünün ağırlığına oranı 0,1’i geçmeyen, iki tekerlekli motosikleti şeklinde ifade edilir."<br><br>


                    <b>A2 sınıfı sürücü belgesi nedir? A2 için yaş sınırı kaçtır?</b> <br><br>

                    2 Tekerlikli motosikletler için: <br>

                    Güç sınırı 35 kv(kilovat)’tır.
                    Kv/kg oranı 0,2 değerini geçmeyecek şekilde sepetli veya sepetsiz olabilir.<br> <br>
                    3 Tekerlikli motosikletler için:<br>

                    Güç sınırı 15 kv(kilovat)’tır.
                    Yaş sınır 18’dir.
                    A2 sınıfı sürücü belgesi olanlar M ve A1 sınıfını kapsayan araçları da kullanabileceklerdir.
                    Yönetmelikte: "Gücü 35 kilovatı ve gücünün ağırlığına oranı 0,2’yi geçmeyen, iki tekerlekli motosiklet şeklinde ifade edilir."<br> <br>

                    <b>A sınıfı sürücü belgesi nedir? A için yaş sınırı kaçtır? Deneyim şartı var mı?</b><br> <br>

                    2 Tekerlikli motosikletler için:<br>

                    Güç sınırı 35 kv(kilovat) üstü olan motosikletleri kullanacaklara verilir.
                    Sepetli veya sepetsiz olabilir.
                    İki tekerlekliler için yaş sınırı 20’dir.
                    3 tekerlikli olup gücü 35 kv(kilovat) aşan motosiklet türleri için de geçerlidir.<br><br>

                    3 Tekerlikliler motosikletler için:
                    Yaş sınırı 21’idr.
                    A sınıfı belge alacakların en az iki yıllık A2 sınıfı deneyimlerinin olması gerekmektedir.
                    Fakat 24 yaşını doldurmuş bir kişi koşulsuz olarak bu ehliyet sınıfı için koşulsuz başvuruda bulunabilir.
                    M, A1 ve A2 sınıfı araçları kullanabildikleri için her türlü motosiklet ve MOPED’leri kullanabilir. En avantajlı ehliyet sınıfı budur.
                    Yönetmelikte: "Gücü 35 kilovatı ve gücünün ağırlığına oranı 0,2’yi geçen iki tekerlekli motosiklet şeklinde ifade edilir."<br><br>

                    <b>B1 sınıfı sürücü belgesi nedir? B1 için yaş sınırı kaçtır?</b><br><br>

                    Normal B grubuyla karıştırılmamalıdır.
                    Motor gücü 15 kv’yi geçmeyen ve ağırlığı 400 kg’dan fazla olmayan dört tekerlikli motosikletleri kullanacaklara verilir.
                    Yük taşımacılığında kullanılması durumunda net ağırlığı 550 kg’ı aşmayan dört tekerlikli motosikletleri kapsar.
                    Elektrik ile çalışanlarda kg hesabında bataryalar dahil edilmez.
                    Yaş sınır 16’dır ve sadece M sınıfı sürücü belgesiyle kullanılabilecek araçları kullanabilir.<br><br><br>

                    <b> B sınıfı sürücü belgesi nedir? B için yaş sınırı kaçtır? B sınıfı belgeye sahip sürücüler hangi araçları kullanabilir?</b> <br><br>

                    Otomobil ve kamyonet kullanacaklara verilir.
                    Yaş sınırı 18’dir.
                    M, B1 ve F sınıfı araçları kullanabilirler.  Motor bisiklet, 4 tekerlik motosiklet ve traktör kullanabilirler.
                    Azami yüklü ağırlığı 4.250 kg olan birleşik araçları kullanabilirler.<br><br>

                    <b> BE sınıf sürücü belgesi nedir? BE için yaş sınırı kaçtır? BE sınıfı belgeye sahip sürücüler hangi araçları kullanabilir?</b><br><br>

                    B sınıfı sürücü belge ile kullanılan araçlarda römorklu yük taşımacılığı yapanlara verilir.  Römork veya yarı römorkun yüklü ağırlığının 3.500 kg’ı geçmemesi esastır.
                    Yaş sınırı 18 olarak belirlemiştir.
                    M, B1, B ve F sınıfı araçları kullanabilirler.
                    B grubu ehliyeti olmayan BE grubuna başvuramaz. B ve BE grubunun yaş sınırı aynı. Bu durumda B aldıktan hemen sonra BE alınabilir. Çünkü yönetmelikte belli bir zaman “deneyim gerekiyor” şartı koşulmamıştır.<br><br>

                    <b>C1 sınıf sürücü belgesi nedir? C1 için yaş sınırı kaçtır? C1 sınıfı belgeye sahip sürücüler hangi araçları kullanabilir?</b><br><br>

                    3500’dan,  7500 Kg’a kadar olan çekici ve kamyon kullanacakların alacağı sürücü belgesi çeşididir.
                    Yaş sınır 18 olarak belirlenmiştir.
                    Deneyim şartı olarak en az B grubu ehliyet olması gerekmektedir.
                    M, B1, B ve F sınıfı araçları kullanabilirler.<br><br>

                    <b>C1E sınıf sürücü belgesi nedir? C1E için yaş sınırı kaçtır? C1E sınıfı belgeye sahip sürücüler hangi araçları kullanabilir?</b><br><br>

                    C1E sınıfı sürücü belgesi C1 sınıfı sürücü belgesiyle kullanılan araçlara takılan ve azami yüklü ağırlığı 750 kilogramı geçen römork veya yarı römorktan oluşan ve katar ağırlığı 12.000 kilogramı geçmeyen birleşik araçlar ile B sınıfı sürücü belgesiyle kullanılan araçlara takılan ve azami yüklü ağırlığı 3.500 kilogramı geçen römork veya yarı römorktan oluşan ve katar ağırlığı 12.000 kilogramı geçmeyen birleşik araçları kullanacaklara verilir.
                    Yaş sınır 18 olarak belirlenmiştir.
                    Deneyim şartı olarak en az C1 grubu ehliyet olması gerekmektedir.
                    M, B1, B, BE, C1 ve F sınıfı araçları kullanabilirler.<br><br>

                    <b>C sınıf sürücü belgesi nedir? C için yaş sınırı kaçtır? C sınıfı belgeye sahip sürücüler hangi araçları kullanabilir?</b><br><br>

                    Kamyon ve çekiciler için geçerlidir.
                    Yaş sınır 21 olarak belirlenmiştir.
                    Deneyim şartı olarak en az B grubu ehliyet olması gerekmektedir.
                    M, B1, B, C1 ve F sınıfı araçları kullanabilirler.<br><br>

                    <b>CE sınıf sürücü belgesi nedir? CE için yaş sınırı kaçtır? CE sınıfı belgeye sahip sürücüler hangi araçları kullanabilir?</b><br><br>

                    CE sınıfı sürücü belgesi C sınıfı sürücü belgesiyle kullanılan araçlara takılan ve azami yüklü ağırlığı 750 kilogramı geçen römork veya yarı römorktan oluşan birleşik araçları kullanacaklara verilir.
                    Yaş sınır 21 olarak belirlenmiştir.
                    Deneyim şartı olarak en az C grubu ehliyet olması gerekmektedir.
                    M, B1, B, BE, C, C1, C1E ve F sınıfı araçları kullanabilirler.
                    CE sınıfı içinde en kapsamlı Ehliyet sınıfıdır.<br><br>

                    <b>D1 sınıf sürücü belgesi nedir? D1 için yaş sınırı kaçtır? D1 sınıfı belgeye sahip sürücüler hangi araçları kullanabilir?</b><br><br>

                    Minibüs kullanacakların alması gereken ehliyet sınıfıdır.
                    Araçların 16+1 kişi yolcu sınırı vardır.
                    Yaş sınırı 21’dir ve en az B grubu ehliyet olması gerekmektedir.
                    M, B, B1 ve F sınıfı dahilindeki araçları kullanabilirler.<br><br>

                    <b>D1E sınıf sürücü belgesi nedir? D1E için yaş sınırı kaçtır? D1E sınıfı belgeye sahip sürücüler hangi araçları kullanabilir?</b><br><br>

                    Araçların 16+1 kişi yolcu sınırı vardır.
                    D1’den farkı, minibüslerin 750 KG’ı geçen römorkları varsa bu ehliyet sınıfını almak gerekir.
                    Yaş sınırı 21’dir ve en az D1 grubu ehliyet olması gerekmektedir.
                    M, B, B1,BE D1 ve F sınıfı dahilindeki araçları kullanabilirler.<br><br>

                    <b> D sınıf sürücü belgesi nedir? D için yaş sınırı kaçtır? D sınıfı belgeye sahip sürücüler hangi araçları kullanabilir?</b><br><br>

                    Otobüs kullanabilmek için gerekli olan ehliyet sınıfıdır.
                    Yaş sınırı 24’dür ve en az B grubu ehliyet olması gerekmektedir.
                    M, B, B1, D1 ve F sınıfı dahilindeki araçları kullanabilirler.<br><br>

                    <b>DE sınıf sürücü belgesi nedir? DE için yaş sınırı kaçtır? DE sınıfı belgeye sahip sürücüler hangi araçları kullanabilir?</b><br><br>

                    Otobüs kullanabilmek için gerekli olan ehliyet sınıfıdır.
                    Yaş sınırı 24’dür ve en az D grubu ehliyet olması gerekmektedir.
                    M, B, B1,BE, D, D1, D1E ve F sınıfı dahilindeki araçları kullanabilirler
                    D sınıfından farkı, otobüslerin 750 KG’ı geçen römorkları varsa bu ehliyet sınıfını almak gerekir.<br><br>

                    <b>F sınıf sürücü belgesi nedir? F için yaş sınırı kaçtır? F sınıfı belgeye sahip sürücüler hangi araçları kullanabilir?</b><br><br>

                    Traktör kullanacaklara verilir.
                    M sınıfı motor bisikletleri de kullanabilir.
                    Yaş sınırı 18’dir.<br><br>

                    <b>G sınıf sürücü belgesi nedir? G için yaş sınırı kaçtır? G sınıfı belgeye sahip sürücüler hangi araçları kullanabilir?</b><br><br>

                    İş makinesi kullanacaklara verilir. Yaş sınırı 18’dir.
                    M sınıfı motor bisikletleri de kullanabilir.
